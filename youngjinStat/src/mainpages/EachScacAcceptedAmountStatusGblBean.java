package mainpages;

public class EachScacAcceptedAmountStatusGblBean {
	private String invoiceDate;
	private String gblNo;
	private String invoiceAmounts;
	private String acceptedAmounts;
	private String accepted;
	private String reason;
	private String code;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getInvoiceDate() {
		return invoiceDate;
	}
	public void setInvoiceDate(String invoiceDate) {
		this.invoiceDate = invoiceDate;
	}
	public String getGblNo() {
		return gblNo;
	}
	public void setGblNo(String gblNo) {
		this.gblNo = gblNo;
	}
	public String getInvoiceAmounts() {
		return invoiceAmounts;
	}
	public void setInvoiceAmounts(String invoiceAmounts) {
		this.invoiceAmounts = invoiceAmounts;
	}
	public String getAcceptedAmounts() {
		return acceptedAmounts;
	}
	public void setAcceptedAmounts(String acceptedAmounts) {
		this.acceptedAmounts = acceptedAmounts;
	}
	public String getAccepted() {
		return accepted;
	}
	public void setAccepted(String accepted) {
		this.accepted = accepted;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
}
