package mainpages;

public class InventoryStatBeans {
	String item="";
	int beginningQuantity=0;
	int beginningAmounts=0; 
	int increaseQuantity=0;
	int increaseAmounts=0;
	int decreaseQuantity=0;
	int decreaseAmounts=0;
	int endingQuantity=0;
	int endingAmounts=0;
	
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public int getBeginningQuantity() {
		return beginningQuantity;
	}
	public void setBeginningQuantity(int beginningQuantity) {
		this.beginningQuantity = beginningQuantity;
	}
	public int getBeginningAmounts() {
		return beginningAmounts;
	}
	public void setBeginningAmounts(int beginningAmounts) {
		this.beginningAmounts = beginningAmounts;
	}
	public int getIncreaseQuantity() {
		return increaseQuantity;
	}
	public void setIncreaseQuantity(int increaseQuantity) {
		this.increaseQuantity = increaseQuantity;
	}
	public int getIncreaseAmounts() {
		return increaseAmounts;
	}
	public void setIncreaseAmounts(int increaseAmounts) {
		this.increaseAmounts = increaseAmounts;
	}
	public int getDecreaseQuantity() {
		return decreaseQuantity;
	}
	public void setDecreaseQuantity(int decreaseQuantity) {
		this.decreaseQuantity = decreaseQuantity;
	}
	public int getDecreaseAmounts() {
		return decreaseAmounts;
	}
	public void setDecreaseAmounts(int decreaseAmounts) {
		this.decreaseAmounts = decreaseAmounts;
	}
	public int getEndingQuantity() {
		return endingQuantity;
	}
	public void setEndingQuantity(int endingQuantity) {
		this.endingQuantity = endingQuantity;
	}
	public int getEndingAmounts() {
		return endingAmounts;
	}
	public void setEndingAmounts(int endingAmounts) {
		this.endingAmounts = endingAmounts;
	}
	
}
